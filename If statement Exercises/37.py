sides=int(input("Ingrese número de lados por favor: "))

if sides==3:
    print("TRIANGULO")
elif sides==4:
    print("CUADRADO")
elif sides==5:
    print("PENTAGONO")
elif sides==6:
    print("HEXAGONO")
elif sides==7:
    print("HEPTAGONO")
elif sides==8:
    print("OCTÓGONO")
elif sides==9:
    print("ENEÁGONO")
elif sides==10:
    print("DECAGONO")
else:
    print("Fuera del rango")